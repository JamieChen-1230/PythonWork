print('--------------第二題--------------')
n1 = [11, 22, 33, 44, 55]
n2 = n1  # n2指向和n1同一個內存
n3 = n1[:]  # 切片是重新複製一份，在創建數據

n1[0] = 666
n3[1] = 999

print(n1)  # => [666, 22, 33, 44, 55]
print(n2)  # => [666, 22, 33, 44, 55]
print(n3)  # => [11, 999, 33, 44, 55]


