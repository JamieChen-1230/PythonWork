print('--------------第一題--------------')
def func(arg, li=[]):
    li.append(arg)
    return li

v1 = func(1)
v2 = func(2, [])
v3 = func(3)

# v1和v3使用同一個列表
# v2使另一個新的列表
print(v1)  # => [1, 3]
print(v2)  # => [2]
print(v3)  # => [1, 3]

